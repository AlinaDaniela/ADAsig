package com.ADAsig.model;

public class Amenzi {
    
    String Categoria;
    String Contraventia;
    int PunctePenalizare;
    int ZileSuspendare;

    public String getCategoria() {
        return Categoria;
    }

    public void setCategoria(String Categoria) {
        this.Categoria = Categoria;
    }

    public String getContraventia() {
        return Contraventia;
    }

    public void setContraventia(String Contraventia) {
        this.Contraventia = Contraventia;
    }

    public int getPunctePenalizare() {
        return PunctePenalizare;
    }

    public void setPunctePenalizare(int PunctePenalizare) {
        this.PunctePenalizare = PunctePenalizare;
    }

    public int getZileSuspendare() {
        return ZileSuspendare;
    }

    public void setZileSuspendare(int ZileSuspendare) {
        this.ZileSuspendare = ZileSuspendare;
    }
    
    
}
